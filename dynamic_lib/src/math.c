float divide(float a, float b) {
    return (a / b);
}

float multiply(float a, float b) {
    return (a * b);
}

float add(float a, float b) {
    return (a + b);
}

float subtract(float a, float b) {
    return (a - b);
}

float _abs(float a) {
    if (a < 0) {
        return (a * 1);
    } else {
        return (a);
    }
}
